package classes;
import pilha.*;

public class Estado {
	private String name;
	private int id;			//	Primary key -  identificador chave.
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	


}
